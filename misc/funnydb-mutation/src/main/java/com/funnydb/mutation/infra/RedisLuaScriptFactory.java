package com.funnydb.mutation.infra;

public final class RedisLuaScriptFactory {
    private RedisLuaScriptFactory() {
    }

    public static String mutationScript() {
        return String.join("\n",
                "-- KEYS[1]: redis document key",
                "-- ARGV[1]: event time",
                "-- ARGV[2]: data lifecycle",
                "-- ARGV[3]: identify",
                "-- ARGV[4]: operate",
                "-- ARGV[5]: properties json",
                "local current = redis.call('GET', KEYS[1])",
                "local doc",
                "if current then",
                "  doc = cjson.decode(current)",
                "else",
                "  doc = {data = {}, metadata = {}}",
                "end",
                "if doc['data'] == nil then doc['data'] = {} end",
                "if doc['metadata'] == nil then doc['metadata'] = {} end",
                "local metadata = doc['metadata']",
                "local data = doc['data']",
                "local eventTime = tonumber(ARGV[1])",
                "local lifecycle = ARGV[2]",
                "local operate = string.lower(ARGV[4])",
                "local properties = cjson.decode(ARGV[5])",
                "local function isNull(value)",
                "  return type(value) == 'userdata'",
                "end",
                "local function detectType(value)",
                "  if isNull(value) then return 'null' end",
                "  local t = type(value)",
                "  if t ~= 'table' then return t end",
                "  return 'array'",
                "end",
                "local function appendIgnored(ignored, field)",
                "  table.insert(ignored, field)",
                "end",
                "local lastEventTime = metadata['last_event_time']",
                "if lastEventTime ~= nil and tonumber(lastEventTime) >= eventTime then",
                "  return {'IGNORED_OLD_EVENT', cjson.encode(doc), '[]'}",
                "end",
                "if metadata['#created_time'] == nil then metadata['#created_time'] = eventTime end",
                "if metadata['#data_lifecycle'] == nil then metadata['#data_lifecycle'] = lifecycle end",
                "local ignored = {}",
                "for field, incomingValue in pairs(properties) do",
                "  local existingValue = data[field]",
                "  local exists = existingValue ~= nil",
                "  if operate == 'add' then",
                "    if type(incomingValue) ~= 'number' then",
                "      appendIgnored(ignored, field)",
                "    elseif not exists or isNull(existingValue) then",
                "      data[field] = incomingValue",
                "    elseif type(existingValue) ~= 'number' then",
                "      appendIgnored(ignored, field)",
                "    else",
                "      data[field] = existingValue + incomingValue",
                "    end",
                "  elseif operate == 'set' then",
                "    if exists and not isNull(existingValue) and not isNull(incomingValue) and detectType(existingValue) ~= detectType(incomingValue) then",
                "      appendIgnored(ignored, field)",
                "    else",
                "      data[field] = incomingValue",
                "    end",
                "  elseif operate == 'setonce' then",
                "    if (not exists) or isNull(existingValue) then",
                "      data[field] = incomingValue",
                "    end",
                "  end",
                "end",
                "metadata['last_event_time'] = eventTime",
                "redis.call('SET', KEYS[1], cjson.encode(doc))",
                "local status = 'APPLIED'",
                "if #ignored > 0 then status = 'INVALID_FIELD_TYPE' end",
                "return {status, cjson.encode(doc), cjson.encode(ignored)}"
        );
    }
}
