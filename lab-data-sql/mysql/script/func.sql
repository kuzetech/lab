
#算数函数
SELECT ABS(-2);
SELECT MOD(101,3);
SELECT ROUND(37.25,1);

#字符串函数
SELECT CONCAT('abc', 123);
SELECT LENGTH('你好'); # 结果=6，utf8 一个汉字当三个字符
SELECT CHAR_LENGTH('你好');# 结果=2，仅计算明面上的字符个数
SELECT LOWER('ABC');
SELECT UPPER('abc');
SELECT REPLACE('fabcd', 'abc', 123);
SELECT SUBSTRING('fabcd', 1,3); # 结果=fab

#日期函数
SELECT CURRENT_DATE(); #结果为 2019-04-03
SELECT CURRENT_TIME(); #结果为 21:26:34
SELECT CURRENT_TIMESTAMP(); #结果为 2019-04-03 21:26:34
SELECT EXTRACT(YEAR FROM '2019-04-03'); #结果为 2019
SELECT DATE('2019-04-01 12:00:05'); #结果为 2019-04-01
SELECT DATE(CURRENT_TIMESTAMP());


#转换函数
SELECT CAST(123.123 AS INT); #结果会报错，CAST 函数在转换数据类型的时候，不会四舍五入
SELECT CAST(123.123 AS DECIMAL(8,2)); #结果为 123.12
SELECT COALESCE(null,3,4); #结果=1，返回第一个非空值


